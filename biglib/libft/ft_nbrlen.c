#include "../lib/libft.h"

size_t	ft_nbrlen(int n)
{
	size_t size;

	size = 0;
	if (n < 0)
		size++;
	while (n / 10 != 0)
	{
		n /= 10;
		size++;
	}
	size++;
	return (size);
}
